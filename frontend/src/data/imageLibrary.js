const DEFAULT_PRODUCT_IMAGE = GENERAL_PHARM_IMAGE;

export function getProductImage(product) {
  const normalizedName = (product?.nombre || "").trim().toLowerCase();
  if (normalizedName && PRODUCT_IMAGE_BY_NAME[normalizedName]) {
    return PRODUCT_IMAGE_BY_NAME[normalizedName];
  }

  const haystack = `${product?.nombre || ""} ${product?.descripcion || ""}`.toLowerCase();
  const found = PRODUCT_IMAGE_RULES.find((rule) => rule.keywords.some((keyword) => haystack.includes(keyword)));
  if (found?.image) {
    return found.image;
  }

  const productId = Number(product?.id);
  if (Number.isInteger(productId) && productId > 0) {
    if (haystack.includes("gato") || haystack.includes("felino")) {
      return FALLBACK_CAT_IMAGES[productId % FALLBACK_CAT_IMAGES.length];
    }
    if (haystack.includes("perro") || haystack.includes("canino")) {
      return FALLBACK_DOG_IMAGES[productId % FALLBACK_DOG_IMAGES.length];
    }
    return FALLBACK_GENERIC_IMAGES[productId % FALLBACK_GENERIC_IMAGES.length];
  }

  return DEFAULT_PRODUCT_IMAGE;
}

